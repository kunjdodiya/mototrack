#!/usr/bin/env node
/**
 * One-shot generator: takes the SVG icon in public/, rasterises it at the
 * sizes Capacitor's asset generator expects, then invokes @capacitor/assets
 * to produce every iOS + Android icon and splash. Re-run any time the source
 * art changes.
 *
 *   npm run native:assets
 *
 * Outputs land in:
 *   ios/App/App/Assets.xcassets/AppIcon.appiconset/
 *   ios/App/App/Assets.xcassets/Splash.imageset/
 *   android/app/src/main/res/mipmap-* and drawable-*
 */
import { mkdir, writeFile, readFile, rename, rm } from 'node:fs/promises'
import { existsSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { execSync } from 'node:child_process'
import sharp from 'sharp'

const here = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(here, '..')
const assetsDir = path.join(repoRoot, 'assets')
const sourceSvg = path.join(repoRoot, 'public', 'icon-512.svg')
const maskableSvg = path.join(repoRoot, 'public', 'icon-maskable-512.svg')

if (!existsSync(sourceSvg)) {
  console.error(`Missing source icon: ${sourceSvg}`)
  process.exit(1)
}

await mkdir(assetsDir, { recursive: true })

const svgBuffer = await readFile(sourceSvg)
const maskableBuffer = existsSync(maskableSvg)
  ? await readFile(maskableSvg)
  : svgBuffer

console.log('Rasterising SVG → 1024×1024 PNGs…')

// icon.png — square, full bleed, used for iOS + as the Android legacy icon
await sharp(svgBuffer, { density: 1024 })
  .resize(1024, 1024)
  .png()
  .toFile(path.join(assetsDir, 'icon.png'))

// icon-only.png — same as icon.png; @capacitor/assets uses this when
// generating Android's adaptive icon background+foreground pair.
await sharp(svgBuffer, { density: 1024 })
  .resize(1024, 1024)
  .png()
  .toFile(path.join(assetsDir, 'icon-only.png'))

// icon-foreground.png — the maskable variant (route stroke alone, no
// background) on a transparent canvas, scaled to 66% so Android's adaptive
// shape mask doesn't clip the art.
await sharp(maskableBuffer, { density: 1024 })
  .resize(1024, 1024)
  .composite([])
  .png()
  .toFile(path.join(assetsDir, 'icon-foreground.png'))

// icon-background.png — flat brand-dark background.
await sharp({
  create: {
    width: 1024,
    height: 1024,
    channels: 4,
    background: { r: 10, g: 10, b: 10, alpha: 1 },
  },
})
  .png()
  .toFile(path.join(assetsDir, 'icon-background.png'))

// splash.png — 2732×2732, icon centred on dark background.
const splashSize = 2732
const splashIconSize = 800
const iconForSplash = await sharp(svgBuffer, { density: 1024 })
  .resize(splashIconSize, splashIconSize)
  .png()
  .toBuffer()

await sharp({
  create: {
    width: splashSize,
    height: splashSize,
    channels: 4,
    background: { r: 10, g: 10, b: 10, alpha: 1 },
  },
})
  .composite([{ input: iconForSplash, gravity: 'center' }])
  .png()
  .toFile(path.join(assetsDir, 'splash.png'))

// splash-dark.png — same dark theme; the app has no light theme.
await sharp({
  create: {
    width: splashSize,
    height: splashSize,
    channels: 4,
    background: { r: 10, g: 10, b: 10, alpha: 1 },
  },
})
  .composite([{ input: iconForSplash, gravity: 'center' }])
  .png()
  .toFile(path.join(assetsDir, 'splash-dark.png'))

console.log('Running @capacitor/assets generate…')
execSync('npx @capacitor/assets generate --iconBackgroundColor "#0a0a0a" --iconBackgroundColorDark "#0a0a0a" --splashBackgroundColor "#0a0a0a" --splashBackgroundColorDark "#0a0a0a"', {
  stdio: 'inherit',
  cwd: repoRoot,
})

// @capacitor/assets writes PWA icons to ./icons relative to the cwd and may
// strip the source SVGs from public/. Restore the SVG sources (we cached the
// bytes above) and relocate the generated PWA icons under public/icons/ so
// they're served by Vite.
await writeFile(sourceSvg, svgBuffer)
await writeFile(maskableSvg, maskableBuffer)

const generatedIconsDir = path.join(repoRoot, 'icons')
const targetIconsDir = path.join(repoRoot, 'public', 'icons')
if (existsSync(generatedIconsDir)) {
  await rm(targetIconsDir, { recursive: true, force: true })
  await rename(generatedIconsDir, targetIconsDir)
  console.log(`Moved PWA icons → ${path.relative(repoRoot, targetIconsDir)}`)
}

await writeFile(
  path.join(assetsDir, 'README.md'),
  `# Native asset sources\n\nGenerated by \`scripts/generate-native-assets.mjs\` from \`public/icon-512.svg\`. Do not hand-edit — re-run \`npm run native:assets\`.\n`,
)

console.log('Done. Run "npm run cap:sync" to copy assets into the native projects.')
